  # returns url without params
  # gets rid of whitespaces first
  def clean_url(url)
    begin
      url = Addressable::URI.escape(url)
      url = "http://#{url}" if Addressable::URI.parse(url).scheme.nil?
      uri = URI(url)
      host = uri.host.downcase

      if host.start_with?('www.')
        host = host[4..-1]
      end

      host + uri.path.downcase
    rescue
      puts "Error: Bad URL"
      url
    end
  end

  def get_host(url)
    begin
      url = "http://#{url}" if Addressable::URI.parse(url).scheme.nil?
      host = Addressable::URI.parse(url).host.downcase
      host.start_with?('www.') ? host[4..-1] : host
    rescue
      puts "Error: Bad URL"
      url
    end
  end
